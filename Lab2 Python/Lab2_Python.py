#Варіант11:визначити чи належить точка заштрихованій частині площини
x=float(input("Введіть координату х:"))#введення координати х
y=float(input("Введіть координату у:"))#введення координати у
if y>x/2 and (x-2)**2+y**2<=4:#умова,за якої точка належить заштрихованій частині площини
    print("точка належить заштрихованій частині площини")#вивід результату
else:#інакше(коли точка не належить площині)
    print("точка не належить заштрихованій частині площини")#вивід результату